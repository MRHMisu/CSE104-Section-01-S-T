#include <stdio.h>
int main()
{
    int a = 2;
    int b = 4;
    printf("%d == %d= %d \n", a, b, a == b); // if true=1 else false =0
    printf("%d > %d= %d \n", a, b, a > b);
    printf("%d >= %d= %d \n", a, b, a >= b);
    printf("%d < %d= %d \n", a, b, a < b);
    printf("%d <= %d= %d \n", a, b, a <= b);
    printf("%d != %d= %d \n", a, b, a != b);
   
}